package cipher.console.oidc.exception;

public class IllegalParamException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public IllegalParamException() {
		super();
	}

	public IllegalParamException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IllegalParamException(String arg0) {
		super(arg0);
	}

	public IllegalParamException(Throwable arg0) {
		super(arg0);
	}

}
